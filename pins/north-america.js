function wdnajsAddEvent(id) {
  var obj = jQuery("#wdnajspins_" + id);
  if(napins_config.pins[id].active === true){
    obj.attr({"cursor": "pointer"});
    obj.hover(function () {
      jQuery("#wddrjstip").show().html(napins_config.pins[id].hover);
      obj.css({"fill":napins_config.pins[id].overColor});
    }, function () {
      jQuery("#wddrjstip").hide();
      obj.css({"fill":napins_config.pins[id].upColor});
    });
    obj.mouseup(function(){
      obj.css({"fill":napins_config.pins[id].overColor});
      if (napins_config.pins[id].target === "new_window"){
        window.open(napins_config.pins[id].url);  
      } else if (napins_config.pins[id].target === "same_window") {
        window.parent.location.href = napins_config.pins[id].url;
      } else if (napins_config.pins[id].target === "modal") {
        jQuery(napins_config.pins[id].url).modal("show");
      }
    });
    obj.mousemove(function (e) {
      var x = e.pageX + 10, y = e.pageY + 15;
      var tipw =jQuery("#wddrjstip").outerWidth(), tiph =jQuery("#wddrjstip").outerHeight(),
      x = (x + tipw >jQuery(document).scrollLeft() +jQuery(window).width())? x - tipw - (20 * 2) : x ;
      y = (y + tiph >jQuery(document).scrollTop() +jQuery(window).height())? jQuery(document).scrollTop() +jQuery(window).height() - tiph - 10 : y ;
      jQuery("#wddrjstip").css({left: x, top: y});
    });
    if (isTouchEnabled()) {
      obj.on("touchstart", function (e) {
        var touch = e.originalEvent.touches[0];
        var x = touch.pageX + 10, y = touch.pageY + 15;
        var tipw=jQuery("#wddrjstip").outerWidth(), tiph=jQuery("#wddrjstip").outerHeight(),
        x = (x + tipw >jQuery(document).scrollLeft() +jQuery(window).width())? x - tipw -(20 * 2) : x ;
        y =(y + tiph >jQuery(document).scrollTop() +jQuery(window).height())? jQuery(document).scrollTop() +jQuery(window).height() -tiph - 10 : y ;
        jQuery("#wddrjstip").show().html(napins_config.pins[id].hover);
        jQuery("#wddrjstip").css({left:x, top:y});
      });
      obj.on("touchend", function () {
        jQuery("#" + id).css({"fill":napins_config.pins[id].upColor});
        if (napins_config.pins[id].target === "new_window") {
          window.open(napins_config.pins[id].url);
        } else if (napins_config.pins[id].target === "same_window") {
          window.parent.location.href = napins_config.pins[id].url;
        } else if (napins_config.pins[id].target === "modal") {
          jQuery(napins_config.pins[id].url).modal("show");
        }
      });
    }
  }
}